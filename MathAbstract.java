package poliMorf;

public abstract class MathAbstract {

	public abstract int multi(int multA, int multB);

	public abstract int divide(int divA, int divB);

	public abstract int plus(int summA, int summB);

	public abstract int minus(int minA, int minB);
	
	public abstract void maximum(Integer...param);
	
	public abstract void minimum(Integer...param);
}
